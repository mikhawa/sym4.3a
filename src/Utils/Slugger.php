namespace App\Utils;

/**
 * Description of Slugger
 *
 * @author AT01
 */
class Slugger {
    public static function slugify(string $string): string
    {
        return preg_replace('/\s+/', '-', mb_strtolower(trim(strip_tags($string)), 'UTF-8'));
    }
}
